/**
 * The Direction enum defines constants for the possible directions an object can move in a 2D space.
 * It is used to standardize the movement directions across the game, ensuring consistency when moving game entities.
 */
public enum Direction {
    UP,    // Represents movement in the upward direction
    DOWN,  // Represents movement in the downward direction
    RIGHT, // Represents movement to the right
    LEFT,  // Represents movement to the left
    NONE   // Represents no movement or a stationary state
}
